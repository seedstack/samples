package org.seedstack.samples.oauth;

import java.util.Map;

/**
 * A user's profile representation to be Mapped to JSON
 */
public class ProfileRepresentation {
    private String userId;
    private String firstName;
    private String lastName;
    private String fullName;
    private String pictureUrl;
    private Map<String, String> principals;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPictureUrl() {
        return pictureUrl;
    }

    public void setPictureUrl(String pictureUrl) {
        this.pictureUrl = pictureUrl;
    }

    public Map<String, String> getPrincipals() {
        return principals;
    }

    public void setPrincipals(Map<String, String> principals) {
        this.principals = principals;
    }
}
